#include <iostream>
#include "complex.hpp"
using namespace std;

int main()
{
    cout<<"*****复数运算*****"<<endl;
    complex a,b;
    cin>>a;
    cin>>b;
    complex r;
    cout<<"和为：";
    r=a+b;
    cout<< r <<endl;
    cout<<"差为：";
    r=a-b;
    cout<< r <<endl;
    cout<<"积为：";
    r=a*b;
    cout<< r <<endl;
    cout<<"商为：";
    r=a/b;
    cout<< r <<endl;
    return 0;


}
